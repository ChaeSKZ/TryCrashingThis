#include "libft.h"

void	*ft_memcpy(void *dest, const void *src, size_t n)
{
	size_t	i;
	char	*str;

	i = 0;
	str = (char *)dest;
	while (i < n)
	{
		*str = *(char *)src;
		str++;
		src++;
		i++;
	}
	return (dest);
}
